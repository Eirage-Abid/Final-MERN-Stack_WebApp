import { Router } from "express";
import { auth } from "../../middleware/auth.js";
import {
  createTask,
  getTasks,
  updateTask,
  deleteTask,
} from "./task.controller.js";

const router = Router();

router.use(auth);

router.post("/", createTask);
router.get("/", getTasks);
router.patch("/:id", updateTask);
router.delete("/:id", deleteTask);

export default router;
